// server/src/users/users.module.ts

/*
 * -----------------------------------------------------------------------------
 * (คำอธิบายไฟล์)
 * ไฟล์นี้คือ "โมดูล" (Module) หรือ "กล่องเครื่องมือ" ของระบบ Users
 *
 * หน้าที่ของมันคือการ "รวบรวม" ทุกสิ่งที่เกี่ยวข้องกับ "ผู้ใช้" มาไว้ด้วยกัน
 * (เช่น "พิมพ์เขียว" (Schema) และ "คนทำงาน" (Service))
 *
 * **(สำคัญที่สุด)** ไฟล์นี้ทำหน้าที่ "ส่งออก" (Export) `UsersService`
 * เพื่อให้ "กล่องอื่น" (เช่น `AuthModule`) สามารถ "ยืม" `UsersService`
 * (ซึ่งเป็นเครื่องมือคุยกับ DB 'users') ไปใช้งานได้
 * -----------------------------------------------------------------------------
 */

import { Module } from '@nestjs/common';
import { MongooseModule } from '@nestjs/mongoose'; // (เครื่องมือคุยกับ DB)
import { User, UserSchema } from './schemas/user.schema'; // (พิมพ์เขียว DB 'users')
import { UsersService } from './users.service'; // (คนทำงาน/สมอง)

@Module({
  // ---------------------------------------------------------------------------
  // (ส่วน "ยืมเครื่องมือ" จากกล่องอื่น)
  // ---------------------------------------------------------------------------
  imports: [
    // 1. "ลงทะเบียน" Models (พิมพ์เขียว) ที่จะใช้กับ Database
    // (MongooseModule.forFeature คือการบอกว่า "ในกล่อง Users นี้")
    // (เราจะมีการ "คุย" กับ Database ใน Collection 'users')
    MongooseModule.forFeature([
      // (เพื่อให้ UsersService สามารถ Inject @InjectModel(User.name) ได้)
      { name: User.name, schema: UserSchema },
    ]),
  ],

  // ---------------------------------------------------------------------------
  // (ส่วน "คนทำงาน" หรือ "ผู้เชี่ยวชาญ" ภายในกล่องนี้)
  // ---------------------------------------------------------------------------
  providers: [
    // 1. "ลงทะเบียน" UsersService
    // (บอก Nest.js ว่า `UsersService` คือ "คนทำงาน" หลักของกล่องนี้)
    UsersService,
  ],

  // ---------------------------------------------------------------------------
  // (ส่วน "ส่งออก" เครื่องมือให้ "กล่องอื่น" ยืม)
  // ---------------------------------------------------------------------------
  exports: [
    // 1. 🚨 (สำคัญ) "ส่งออก" UsersService 🚨
    // (โดยปกติ "คนทำงาน" (Service) จะเป็น "ของส่วนตัว" (private) ของกล่องนั้นๆ)
    // (การ `export` คือการ "อนุญาต" ให้ "กล่องอื่น" ที่ `import UsersModule` นี้)
    // (สามารถ "ฉีด" (Inject) และ "เรียกใช้" `UsersService` ได้โดยตรง)
    UsersService,
  ],
})
export class UsersModule {}

/*
 * -----------------------------------------------------------------------------
 * (สรุปการทำงาน)
 * 1. `UsersModule` (ไฟล์นี้) "สร้าง" `UsersService` และ "ส่งออก" (exports)
 * 2. `AuthModule` (อีกไฟล์) "ยืม" `UsersModule` (imports: [UsersModule])
 * 3. `AuthService` (ในกล่อง Auth) จึงสามารถ "ฉีด" (Inject) `UsersService` ได้
 * 4. `AuthService` จึงสามารถเรียก `this.usersService.findByEmail(...)`
 * หรือ `this.usersService.create(...)` ได้
 * -----------------------------------------------------------------------------
 */
